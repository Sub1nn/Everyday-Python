x = 5
y = 10

# Equality comparison
print(x == y)  # False

# Inequality comparison
print(x != y)  # True

# Greater than comparison
print(x > y)  # False

# Less than comparison
print(x < y)  # True

# Greater than or equal to comparison
print(x >= y)  # False

# Less than or equal to comparison
print(x <= y)  # True

# Chain comparison
print(x < y < 15)  # True
