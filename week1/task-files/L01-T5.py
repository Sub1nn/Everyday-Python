first_int = int(input("Give the first integer:\n"))
second_int = int(input("Give the second integer:\n"))

addition = first_int + second_int
subtraction = first_int - second_int
multiplication = first_int * second_int
division = first_int / second_int
floor_div = first_int // second_int
exponents = first_int ** second_int
modulus = first_int % second_int

print(f"{first_int} + {second_int} = {addition}")
print(f"{first_int} - {second_int} = {subtraction}")
print(f"{first_int} * {second_int} = {multiplication}")
print(f"{first_int} / {second_int} = {division}")
print(f"{first_int} // {second_int} = {floor_div}")
print(f"{first_int} ** {second_int} = {exponents}")
print(f"{first_int} % {second_int} = {modulus}")
